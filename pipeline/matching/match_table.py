"""
Two-layer regulation table for pipeline.

Architecture
------------
Every synopsis row maps to exactly one entry.  There are two layers:

    BaseEntry    — auto-generated by running the v2 linker on
                   synopsis_raw_data.json.  Contains only gnis_ids — the
                   stable identifier from natural name search.  Everything
                   else (geometry, edge_ids, poly_ids) is resolved at
                   runtime from gnis_id by downstream pipeline steps.

    OverrideEntry — manually authored corrections (source of truth:
                    overrides.json).  An OverrideEntry for a given
                    (region, name_verbatim) always wins over a BaseEntry.
                    Can use gnis_ids, waterbody_keys, fwa_watershed_codes,
                    or admin_targets as identifiers.

Both layers inherit from _EntryBase which provides common fields:
    criteria (MatchCriteria), gnis_ids, name_variants.

All ``fwa_watershed_codes`` are normalised via ``trim_wsc()`` on load
so padded values in overrides.json match trimmed atlas records.

Lookup key
----------
Index key is (region, name_verbatim, frozenset(mus)).  All three fields
must match exactly — when a synopsis row changes its MUs, the match breaks
visibly instead of silently returning a stale entry.

Name variants
-------------
One entry can cover multiple synopsis names via name_variants.  Each variant
shares the same region as the primary criteria.  Both the primary name and all
variants are indexed so lookup always returns the same entry regardless of
which name appears in the synopsis row.
"""

from __future__ import annotations

import logging
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Dict, List, Optional, Union

from pipeline.utils.wsc import trim_wsc

from .reg_models import MatchCriteria

